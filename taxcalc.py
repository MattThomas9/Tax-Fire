def taxcalc(txblinc, txbrk, ntxbrk):
    c = []
    tax = 0.0
    for i in range(0, ntxbrk):
        if i == 0:
            c.append(0.0)
        else:
            c.append(c[i - 1] + (txbrk[i - 1][1] - txbrk[i - 1][0]) * (txbrk[i - 1][2] / 100))
        if txblinc <= txbrk[i][1]:
            tax = c[i] + (txblinc - txbrk[i][0]) * (txbrk[i][2] / 100)
            break
    return tax
